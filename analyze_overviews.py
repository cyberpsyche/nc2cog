#!/usr/bin/env python3
"""
分析gdalinfo输出并解释为什么实际概览层级数少于指定的层级数
"""

def analyze_gdalinfo():
    print("=== GDAL 概览层级生成机制分析 ===\n")

    print("根据您提供的 gdalinfo 输出:")
    print("  - 原始图像尺寸: 1781 x 1572")
    print("  - 指定概览层级: 2, 4, 8, 16, 32")
    print("  - 实际生成概览: 890x786, 445x393 (相当于缩小比例 ~2 和 ~4)")
    print()

    print("原因分析:")
    print("1. 尺寸限制:")
    print(f"   - 原始尺寸: {1781} x {1572}")
    print(f"   - 理论层级2: {1781//2} x {1572//2} = 890 x 786 ✓ 存在")
    print(f"   - 理论层级4: {1781//4} x {1572//4} = 445 x 393 ✓ 存在")
    print(f"   - 理论层级8: {1781//8} x {1572//8} = 222 x 196")
    print(f"   - 理论层级16: {1781//16} x {1572//16} = 111 x 98")
    print(f"   - 理论层级32: {1781//32} x {1572//32} = 55 x 49")
    print()

    print("2. GDAL 自动优化规则:")
    print("   - GDAL 不会为太小的概览层级生成文件")
    print("   - 通常最小概览尺寸不能小于某个阈值 (通常是 128x128 或更小)")
    print("   - 对于 1781x1572 的图像，8、16、32 的缩小级别产生的图像太小")
    print("   - GDAL 只生成有意义的概览层级 (保持一定分辨率)")
    print()

    print("3. 实际概览生成逻辑:")
    print("   - 层级2: 890x786 (保留 - 合适的分辨率)")
    print("   - 层级4: 445x393 (保留 - 合适的分辨率)")
    print("   - 层级8: 222x196 (可能被GDAL认为太小或自动合并)")
    print("   - 层级16: 111x98 (可能被过滤掉，因为太小)")
    print("   - 层级32: 55x49 (被过滤掉，因为太小)")
    print()

    print("4. COG 优化策略:")
    print("   - COG 格式会根据原始数据尺寸智能选择概览层级")
    print("   - 不一定会严格按照指定的数字生成，而是生成最有用的层级")
    print("   - 目标是在文件大小和浏览性能之间取得平衡")
    print()

    print("建议:")
    print("   - 对于较小的图像，使用较少的概览层级 (如 2,4)")
    print("   - 对于较大的图像，可以使用更多的概览层级 (如 2,4,8,16,32,64)")
    print("   - GDAL 会自动选择最佳的概览策略")

if __name__ == "__main__":
    analyze_gdalinfo()