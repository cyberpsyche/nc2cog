#!/usr/bin/env python3
"""
详细解释nc2cog中概览层级的工作原理
"""

def explain_overview_levels():
    print("=== nc2cog 概览层级 (Overview Levels) 工作原理解释 ===\n")

    print("1. 概览层级参数说明：")
    print("   --overview-levels 2,4,8,16,32")
    print()

    print("2. 参数含义：")
    print("   - 每个数字表示缩小比例因子")
    print("   - 2: 图像分辨率缩小为原始的 1/2 (像素密度为原始的 1/4)")
    print("   - 4: 图像分辨率缩小为原始的 1/4 (像素密度为原始的 1/16)")
    print("   - 8: 图像分辨率缩小为原始的 1/8 (像素密度为原始的 1/64)")
    print("   - 16: 图像分辨率缩小为原始的 1/16 (像素密度为原始的 1/256)")
    print("   - 32: 图像分辨率缩小为原始的 1/32 (像素密度为原始的 1/1024)")
    print()

    print("3. 数据层级构成：")
    print("   - 第 1 层: 原始分辨率 (全分辨率数据，例如 1000x1000 像素)")
    print("   - 第 2 层: 概览层级 2 (约 500x500 像素，如果原始是1000x1000)")
    print("   - 第 3 层: 概览层级 4 (约 250x250 像素)")
    print("   - 第 4 层: 概览层级 8 (约 125x125 像素)")
    print("   - 第 5 层: 概览层级 16 (约 62x62 像素)")
    print("   - 第 6 层: 概览层级 32 (约 31x31 像素)")
    print()

    print("4. 正确理解：")
    print("   ✓ 您的参数 --overview-levels 2,4,8,16,32 会生成 5 个概览层级")
    print("   ✓ 加上原始数据层，总共是 6 个可访问的数据层级")
    print("   ✓ 这些概览是通过重采样原始数据生成的抽样数据")
    print()

    print("5. 实际应用：")
    print("   - 当用户在GIS软件中放大时，使用高分辨率层 (接近原始分辨率)")
    print("   - 当用户缩小或概览全局时，使用低分辨率层 (如1/32分辨率)")
    print("   - 这样大大提高了大数据集的可视化性能")
    print()

    print("6. 建议配置：")
    print("   - 科学数据: --overview-levels 2,4,8,16,32,64")
    print("   - 标准用途: --overview-levels 2,4,8,16 (默认)")
    print("   - 快速浏览: --overview-levels 2,4,8")
    print()

    print("注意: 生成更多层级会增加文件大小，但提高多尺度浏览体验；")
    print("      生成较少层级会减少文件大小，但多尺度浏览体验有限。")

if __name__ == "__main__":
    explain_overview_levels()